    private void btnModificarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarVendedorActionPerformed
     if(txtCodigo.getText().trim().isEmpty()){
            mostrarMensajeError("Llena el valor A");
            return;
        }
        
        if(txtNombre.getText().trim().isEmpty()){
            mostrarMensajeError("Llena el valor B");
            return;
        }
        
        double a = Double.parseDouble(txtCodigo.getText());
        double b = Double.parseDouble(txtNombre.getText());
        
        
        mostrarMensajeExito(controlador.getUsuario()+ " realizo una resta. La respuesta es: " + controlador.restar(a, b));
        limpiar();
    }//GEN-LAST:event_btnModificarVendedorActionPerformed

