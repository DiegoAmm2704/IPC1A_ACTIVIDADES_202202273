    private void btnModificarVendedor3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarVendedor3ActionPerformed
       if(txtCodigo.getText().trim().isEmpty()){
            mostrarMensajeError("Llena el valor A");
            return;
        }
        
        if(txtNombre.getText().trim().isEmpty()){
            mostrarMensajeError("Llena el valor B");
            return;
        }
        
        double a = Double.parseDouble(txtCodigo.getText());
        double b = Double.parseDouble(txtNombre.getText());
        
        if(b == 0){
            mostrarMensajeError("B no puede ser 0");
            return;
        }
        
        mostrarMensajeExito(controlador.getUsuario()+ " realizo una division. La respuesta es: " + controlador.dividir(a, b));
        limpiar();
    }//GEN-LAST:event_btnModificarVendedor3ActionPerformed

